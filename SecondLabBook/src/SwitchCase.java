
public class SwitchCase {

	public static void main(String[] args) {
		
		int t= 2;

		 switch(t){
		case 1:
			System.out.println("java");
		    break;
		 
		 case 2:
			 System.out.println("ES6");
		     break;
		 
		 default:
			 System.out.println("Not Listed..");
		 
		 }

	}

}
